/**
 * Track Firebase authentication status in React components.
 *
 * STATE RETURNED:
 * - user:           Firebase User object | null
 * - isLoading:      true ‡∏à‡∏ô‡∏Å‡∏ß‡πà‡∏≤ auth state ‡∏ñ‡∏π‡∏Å‡∏Å‡∏≥‡∏´‡∏ô‡∏î
 * - error:          string | null ‚Äì ‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° error ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ
 */
export function useAuthState() {
  const [user, setUser] = useState<User | null>(null)
  const [isLoading, setIsLoading] = useState<boolean>(true)
  const [error, setError] = useState<string | null>(null)

  useEffect(() => {
    // If Firebase not initialised (e.g. dev without env vars) ‚Äì skip
    if (!auth) {
      console.warn("üîß Firebase auth not available, using mock state")
      setIsLoading(false)
      return
    }

    // Subscribe to auth state changes
    const unsubscribe = onAuthStateChanged(
      auth,
      (currentUser) => {
        setUser(currentUser)
        setIsLoading(false)
      },
      (err) => {
        console.error("üî• Auth: onAuthStateChanged error", err)
        setError("‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ")
        setIsLoading(false)
      },
    )

    // Cleanup on unmount
    return unsubscribe
  }, [])

  return { user, isLoading, error }
}

